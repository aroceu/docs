<h1 id="pa">Privacy & Accessibility</h1>

<p>I believe in privacy and accessibility as much as I am passionate about creative authenticity. 
	With my websites, I have done my best to strike a balance between how imperfect I allow myself to be, 
	and how visitor-accessible my sites are.</p>

<p>In terms of privacy, I've made my websites as uninterested in visitor information or financial revenue as I am. 
	This includes:</p>

<ul>
	<li>Not using tracking cookies for analytics.</li>
	<li>No third-party advertisements, sponsorships, corporate endorsements, or pop-ups.</li>
	<li>Limited visitor input, feedback, and tracking.</li>
</ul>

<p>As a developer, I frequently work with the mindset of "I'll make this better later," so sometimes my websites are not 
	fully accessible upon launch, or when online. At any given moment, I have either done my best or aspire to make my websites 
	optimally:</p>

<ul>
	<li>Work responsively for desktop, tablet, and mobile screen resolutions; or have alternative options. 
		(See <a href="#exceptions">exceptions</a>).</li>
	<li>Use semantic HTML as much as I am intuitively capable of.</li>
	<li>Allow a night-mode option for higher contrast ratios on select pages that contain large blocks of text and 
		where visitor duration may be longer.</li>
	<li>Use a minimal amount of flashing animations and GIFs, and/or allow visitors to start/stop animations at will.</li>
	<li>Not use any audio on webpages, auto-play or otherwise.</li>
	<li>Use alt text as frequently and accurately as possible. (See <a href="#exceptions">exceptions</a>.)</li>

</ul>
<p>Additionally, my websites:</p>

<ul>
	<li>Use JavaScript.</li>
	<li>Make simple use of iframes to convey a small amount of information between separated content.</li>
	<li>Do not make any use of AI-generated text, images, or content.</li>
	<li>Allow little to no access for AI scraping, web crawlers, and bots. I don't mind being shared or archived but I don't want my data to be commodified either.</li>
</ul>

<p>Websites I link to consist of their own content and webmasters, which I have no interest in policing. 
	My principles are in place as personal boundaries, and are always subject to flexibility. 
	I do not implicitly endorse opinions and views on linked websites that are not expressly mine.</p>

<p>I regularly link to external websites, and sometimes in a new tab or window. 
	If you click on a link and are unable to see a change in the webpage, 
	be sure to check your browser's tabs or windows to see if the page has opened externally.</p>

<p>Lastly, a good portion of the content on my websites is fannish, creative, fictional, and primarily for my own enjoyment. 
	Though I recognize that I am responsible for what I put on the internet, I am also a big believer of all visitors having 
	the agency to navigate away from my websites per their own levels of comfort. 
	I wish for my websites to be accessible, but this does not mean that I intend for my content to be for everyone. 
	Please keep this in mind when perusing.

<h2 id="exceptions">Exceptions</h2>

<p>Due to the way I like to design, a handful of webpages are knowingly not fully accessible, 
	either because they are currently a work in progress, or because of the selective style of the webpage.</p>

<p>This includes:</p>

<ul>
	<li>My music subsection is built with tables so its responsive appeal is limited due to that. 
		However, its content is still readable and contains a notice when viewing on less than optimal screen sizes.</li>
	<li>My manifesto page makes use of an animated background for visual effects without disrupting its readability. 
		It is also compatible with reader view as an alternative.</li>
	<li>As I am someone with a strong preference for pastel colors on white, some webpages may have color contrast 
		ratios that are not considered fully accessible. However, most of these pages would not require the visitor
		to read for longer than a minute or so. Sites requiring visitors to read at length have a night mode 
		option with a higher contrast ratio.</li>
	<li>I do not use "Skip to Main Content" for screenreaders, as most of my websites differ too vastly in design or 
		do not consist of a lot of text to be read or skipped. Projects that do will have it implemented eventually.</li>
	<li>I design through and for computers wherein mouse-hovering is the norm, so while most of my designs may be 
		responsive, they are generally best suited for a mouse-and-screen interface.</li>
	<li>Making designs responsive is a bit of a chore for me, so sometimes I launch a project before designing it responsively.
		I intend for them to be responsive eventually, but in my own time.</li>
	<li>Additional projects may contain their own warnings regarding accessibility.</li>
</ul>

<p>For over a decade and a half in my webdesign hobby, adding alt text to images was not a practice I frequented. 
	I am doing my best to improve this habit and provide accurate alt text on both past and present/future projects. 
	I would appreciate patience in the meantime.</p>

