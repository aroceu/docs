<p>The one and probably only webring I will own, born out of my desire in early December 2025 to join a fandom-related webring relevant to my interests, while also wanting to make a webring myself. Concluding to make this webring occurred after a few days of browsing others, but hey, at least I got here eventually.</p>

<p>The <strong>fandom webring</strong> has turned into an easy method for me to discover new personal websites to check without my needing to browse for them, but rather through people applying to join and me going, "ooh! a new website to check out!" with each email and eagerly clicking each new link. I've never been so enthusiastic about vetting before.</p>

<p>I designed this site to be reminiscent of early personal sites to appeal to my internet elders, with modern flourishes to appeal to newer webdesigners as well. The old-school elements are broader with the bookish look, a more aged aesthetic, and simple one column layout. Modern design elements are expressed through stylistic decoration with the washi tape, images in some headings, and wavy underlined links.</p>

