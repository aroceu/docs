<h1 id="purpose">Existential Purpose</h1>

<p>My websites exist to:</p>

<ul>
	<li>Present me and the things I like/feel strongly about in ways that make sense and/or appeal to me.</li>
	<li>Be a fun and independent space on the internet where no one's financial interests are even remotely relevant.</li>
	<li>Evolve into interesting projects that past, present, and future versions of me will find cool, if not stepping stones in some way.</li>
	<li>Embody my core principles of: uniqueness of the human experience through creativity, patience and growth,  autonomy and self-celebration without corporate or systemic interest, the importance of personal choice and individual curation, authenticity and integrity, intention and interpretation, and being driven by empathetic curiosity.</li>
</ul>

<p>I make websites:</p>

<ul>
	<li>To personalize and custom design an expansive digital space that is uniquely mine.</li>
	<li>To celebrate things I make and things I like; to be unashamed of myself in ways I cannot or do not want to change.</li>
	<li>To not depend on social media as a representation of who I am. Social media has always been about interaction for me, but that's just one way I can talk to people on the internet. There are dozens more, and even more ways I can have fun on the web, without social media.</li>
	<li>To discover who I am, what I like, what I am inclined to, and what I want. In other words: to be me.</li>
	<li>To expand my capabilities with a skill I didn't even really intend to learn with so much depth in the first place.</li>
	<li>With love, much like the way I live.</li>
</ul>

