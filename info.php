<h2 id="kingdra">kingdra.net</h2>

<blockquote><p><strong>Established:</strong> 19 April 2015</p>
<p><strong>Version:</strong> 1.0 <small>(<em>"the world's most impractical site"</em>)</small></p>
<p><strong>Main Colors:</strong> Ceruleans, creams, yellows</p>
<p><strong>Main Fonts:</strong> Inconsolata</p>
</blockquote>

<p>
I refer to this as my portal, or network of websites. I generally think of the site as "what the inside of my brain looks like."</p> 

<p>As my love for making websites stems beyond projects reflecting who I am at my core,
    <strong>kingdra.net</strong> serves to bridge the gap between me and other websites, or me and other people.
    Similarly, it hosts projects that do not (or no longer) feel as personal to me as the projects on
    <code>aroceu.com</code>, but I still find them valuable or interesting to share.
</p>

<p>
This was a placeholder domain for the longest time, mostly because I wanted to like Kingdra more as a Pokémon and in 2015, I only liked 
the idea of owning this specific domain. I didn't launch a real design for the main page until 26 October 2024, during the third 
significant wave of a webdesign hyperfixation in my adult life, when I realized that I wanted to expand my passion for making 
websites beyond projects that are intimately tied to who I am. This was my biggest mental block when it came to my websites 
in the past - while I like making things intrinsically tied to who I am, those aren't the <em>only</em> projects I like to make. 
It's psychically uncomfortable for me to have projects that feel less "personal" under an umbrella domain that is ultimately 
personal. When I do, I feel like I'm lying to myself as well as other people.
</p>

<p>
So kingdra.net exists for those specific neuroses. It sat around for about nine years with random subprojects not fit 
for my personal site, but through the aforementioned hyperfixation, expanding it became necessary the more I thought 
about how I wanted to share and participate in the independent web. Plus, I grew to like Kingdra as a Pokémon a lot more 
than I did in 2015. I kind of tricked myself into liking it more after owning this domain for so long, but I think it
was meant to be simply because I <em>wanted</em> to like it.
</p>

<p>
At one point this site had its own documentation. As of late July 2025, I've scrapped it and rehashed the important details here:
</p>

<ul>
	<li>The main desktop page is set up with three columns and top navigation because on my first website, the layout I'd made for it and liked the most was structured in the same way.</li>
	<li>Similarly, the main desktop uses iframes identically to that layout on my first website. I also find it practical for this site, in curating an aimless scavenger hunt with a bunch of smaller pages to find and view in a fixed frame like a portal.</li>
	<li>The background came from my retired <code>stuff</code> section, which I liked a lot and wanted to find another reason to use it. Luckily, I launched the index of kingdra.net shortly after retiring <code>stuff</code>.</li>
	<li>All the stamps and linkback buttons existed briefly in my personal site's library before I moved them here.</li>
	<li>I use the same notebook background here as I do on my personal log and other sections, because I've always loved notebooks. When I was younger, I actually made a handful of themes based on my love for notebooks, so every thing I reuse this background is a testament to that.</li>
	<li>While I am not a furry, the way I feel about my splatsona is similar, in terms of having an externalized persona. I say this because my inspiration for having my splatsona in the corner was from visiting a lot of furry websites where people's fursonas had a similar function.</li>
	<li>My favorite thing about the indie web has always been visiting other people's websites; I used to ask to affiliate a <em>lot</em> when I was younger, mostly as a bookmark to visit people's sites again. In that time, the internet has expanded and I've grown more reclusive. But I still love visiting people's websites with the same fervor, which is why I have such an extensive button wall.</li>
	<li>Once I hit a hundred buttons on the page, though, I realized it might be a little impractical for really anyone else to visit. So now I have a random rotation of five on each page load, because I want people to be as interested in visiting those personal sites as I am.</li>
	<li>This website is primarily for me to maintain and keep track of things related to my websites and beyond, such as things I make irregularly, my favorite albums, and old flash games I like being nostalgic for, in addition to more housekeeping things like network updates and future plans. This is part of why the desktop design is so impractical - the only audience it serves to appeal to is me.</li>
	</ul>

<p>My main projects under <code>aroceu.com</code> are linked at the top of the desktop page because it is ultimately still my main website. Additional domain projects can be found on the side, and the iframe navigation at the bottom links to the most distinct and necessary pages. The mobile/text-only alternative lists all internal or network pages linked from the desktop.</p>

