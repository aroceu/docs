<p><strong>aroceu.com</strong> is my primary and personal domain. I often think of it as "me in website form." It consists of a variety of different projects, each with their own unique design and intent in existing.</p>

<p>I chose <code>.com</code> as the TLD for this site because it's the most common one, and therefore easy to intuit instead of needing to remember. Despite my aesthetic tendencies, I would still like to be accessible, to some degree. However, I'm not super fond of <code>.com</code> as a Bluesky handle, which is why my Bluesky (and email) domain is <strong>aroceu.cloud</strong> instead.</p>

<p>The landing page is a simple, branching root connecting them all with the appearance of minimalism: the first impression you'll often get of me. It's set up this way - domain and all - because it's the URL I give out the most, so the social links make it easy to contact me elsewhere. Other information and links on this page are things that I think are necessary for that first impression, and perhaps to pique a bit of curiosity.</p>

<p>Everything housed here is its own individual subproject in some way, because I think of my identity as an amalgamation of things I love as well as things that make me uniquely me. I'm categorical by nature, and love the thrill of entering a new webpage and discovering a new aesthetic or design. At my core I feel prismatic, so the way I lay out the projects under this domain helps to reflect that.</p>

<p>For the sake of organization, I've grouped the projects under this domain into four primary categories. Each is relevant to who I am under this alias:</p>

<ol>
	<li>Personal - Me, Log, Gifts</li>
	<li>Creative - Fic, Projects, Prompt Generators</li>
	<li>Fannish - Library, Movies, Music, subsections dedicated to specific media I like</li>
	<li>Internet - Docs, Atlas, Manifesto</li>
</ol>

